metros = float(input("Olá, digite um número em metros: "))

decimetros = metros * 10

centimetros = metros * 100

milimetros = metros * 1000

print("Olá, o número em metros que você digitou é: {} m \n Em decímetros ele é: {} dm \n Em centímetros ele é: {} cm \n E em milímetros é: {} mm".format(metros,decimetros,centimetros,milimetros))
